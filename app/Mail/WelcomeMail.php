<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;
use App\Models\User;

class WelcomeMail extends Mailable
{
    use Queueable, SerializesModels;

    public $user;

    /**
     * Create a new message instance.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        // You could add any additional dynamic content, such as personalized greetings or user-specific data.

        return $this->subject("Welcome to Fittelly â€” Let's get started")
            ->view('emails.welcome')
            ->with([
                'user' => $this->user,
            ]);
    }
}
